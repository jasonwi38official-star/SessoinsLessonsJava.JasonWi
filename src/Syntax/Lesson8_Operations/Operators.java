package Syntax.Lesson8_Operations;

public class Operators {
    public static void main(String[] args) {
        int a = 10, b = 3;
        //Оператор присваивания =.
        b = a;


        //арифметические операторы
        int sum = a + b;
        int difference = b - a;
        int produсt = a * b;
        int quotient = a / b;
        int remainder = a % b;

        //a=a+1;
        a++; //Инкрементирование (+1)
        a--; //Дикрементирование (-1)


        a += 4; //a=a+4;
        a -= 4; //a=a-4;
        a *= 4; //a=a*4;
        a /= 4; //a=a/4;


    }
}
